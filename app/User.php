class User extends Authenticatable implements HasMedia
{
    use Notifiable, HasMediaTrait, HasRoles;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name', 'email', 'password', 'duty','mobile'
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password', 'remember_token',
    ];

    protected $appends = ['avatar'];

    public function getAvatarAttribute() {

        if ($this->getMedia('avatar')->isNotEmpty()) {
            return $this->getFirstMedia('avatar')->getFullUrl();
        }
        $pinyin = new Pinyin();
        $name = $pinyin->abbr($this->name);
        if (empty($name)) {
            $name = $this->name;
        }
        $name = \Avatar::create($name)->toBase64();
        return $name;
    }

}
